package com.example.demo.models;

public class FormAttributes {

    private String city;

    public FormAttributes() {
        super();
    }

    public FormAttributes(String city) {
        super();
        this.city = city;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }
}
